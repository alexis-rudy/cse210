public class Breathing : Activity{
    private string _breathIn = "Breath in...";
    private string _breathOut = "Breath out...";

    public string GetBreathIn(){
        string breathIn;
        _breathIn = breathIn;
        return breathIn;
    }

    public string GetBreathOut(){
        string breathOut;
        _breathOut = breathOut;
        return breathOut;
    }

}